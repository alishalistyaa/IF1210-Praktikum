{-
-- Definisi dan Spesifikasi
isPositif :: Int -> Bool 
    -- isPositif bernilai true jika x>0
-- Realisasi
isPositif x = x>=0

-- Contoh aplikasi
-- isPositif 2
-}

{-}
-- Definisi dan spesifikasi
isAnA :: Char -> Bool 
    -- isAnA bernilai true jika c ="A"
-- Realisasi
isAnA c = c=='A'
-- Contoh aplikasi
-- isAnA b
-}


-- Definisi dan spesifikasi
isValid :: Int -> Bool 
    -- isValid bernilai true jika x<5 atau x>500
-- Realisasi
isValid x = (x<5)||(x>500)
--Contoh aplikasi
-- isValid 54


